"""Немного статистики — ровно столько, чтобы не выдать шум за результат.

Всё на стандартной библиотеке: math.comb хватает и на точный тест Макнемара,
и на интервал Уилсона. Ни numpy, ни scipy сюда не нужны.

Почему именно Макнемар: план парный — один и тот же артефакт судят все четыре
ячейки. Значит, сравнивать доли как независимые выборки нельзя, работает
только расхождение внутри пар. Точная (биномиальная) форма выбрана потому, что
на десятках наблюдений приближение хи-квадрат врёт.
"""

from __future__ import annotations

import math
from dataclasses import dataclass

Z95 = 1.96
ПОРОГ_ЗНАЧИМОСТИ = 0.05


@dataclass(frozen=True)
class Доля:
    """Сколько одобрено из скольких, с интервалом Уилсона."""

    одобрено: int
    всего: int

    @property
    def доля(self) -> float:
        return self.одобрено / self.всего if self.всего else 0.0

    @property
    def процент(self) -> float:
        return 100.0 * self.доля

    @property
    def интервал(self) -> tuple[float, float]:
        return уилсон(self.одобрено, self.всего)

    @property
    def кратко(self) -> str:
        """Без интервала — для клеток таблицы, где важна ровная колонка."""
        if not self.всего:
            return "нет данных"
        return f"{self.одобрено}/{self.всего} = {self.процент:>3.0f}%"

    def __str__(self) -> str:
        if not self.всего:
            return "нет данных"
        низ, верх = self.интервал
        return (
            f"{self.одобрено}/{self.всего} = {self.процент:.0f}% "
            f"[95% {100 * низ:.0f}–{100 * верх:.0f}]"
        )


@dataclass(frozen=True)
class Макнемар:
    """Расхождения в парах и точная двусторонняя вероятность."""

    пар: int
    первый_да_второй_нет: int
    первый_нет_второй_да: int
    p: float

    @property
    def расхождений(self) -> int:
        return self.первый_да_второй_нет + self.первый_нет_второй_да

    @property
    def значимо(self) -> bool:
        return self.p < ПОРОГ_ЗНАЧИМОСТИ


def уилсон(одобрено: int, всего: int, z: float = Z95) -> tuple[float, float]:
    """Интервал Уилсона: на малых выборках честнее обычного нормального."""
    if всего <= 0:
        return (0.0, 0.0)
    доля = одобрено / всего
    знаменатель = 1.0 + z * z / всего
    центр = (доля + z * z / (2 * всего)) / знаменатель
    разброс = (
        z
        * math.sqrt(доля * (1 - доля) / всего + z * z / (4 * всего * всего))
        / знаменатель
    )
    return (max(0.0, центр - разброс), min(1.0, центр + разброс))


def двусторонняя_биномиальная(меньшее: int, всего: int) -> float:
    """P(расхождения так же перекошены или сильнее) при честной монете."""
    if всего <= 0:
        return 1.0
    хвост = sum(math.comb(всего, k) for k in range(меньшее + 1)) / (2**всего)
    return min(1.0, 2.0 * хвост)


def макнемар(пары: list[tuple[bool, bool]]) -> Макнемар:
    """Парное сравнение двух ячеек по одним и тем же артефактам."""
    да_нет = sum(1 for первый, второй in пары if первый and not второй)
    нет_да = sum(1 for первый, второй in пары if not первый and второй)
    p = двусторонняя_биномиальная(min(да_нет, нет_да), да_нет + нет_да)
    return Макнемар(len(пары), да_нет, нет_да, p)
