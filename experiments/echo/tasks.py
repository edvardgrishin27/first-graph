_СПЕКА_ОБРЕЗАТЬ = """Модуль сохраняется как «решение.py». В нём одна функция:

    def обрезать(текст: str, предел_байт: int) -> str

Требования:
1. Возвращает самый длинный ПРЕФИКС текста, который в кодировке UTF-8
   занимает не больше предел_байт байт.
2. Символ нельзя разрывать посередине: результат — корректная строка,
   а не половина многобайтового символа.
3. Если предел_байт отрицательный — ValueError; если ноль — пустая строка.
4. Ничего не дописывать: никаких многоточий и «...».
5. Только стандартная библиотека."""

_ТЕСТ_ОБРЕЗАТЬ = '''
import pytest

from решение import обрезать


def test_кириллица_считается_в_байтах():
    # «п», «р», «и» — по два байта: в 5 байт влезают только два символа.
    assert обрезать("привет", 5) == "пр"


def test_эмодзи_не_разрывается():
    # «👍» занимает четыре байта.
    assert обрезать("a👍b", 2) == "a"
    assert обрезать("a👍b", 5) == "a👍"


def test_целиком_влезает():
    assert обрезать("abc", 10) == "abc"


def test_границы():
    assert обрезать("привет", 0) == ""
    assert обрезать("", 5) == ""
    with pytest.raises(ValueError):
        обрезать("abc", -1)
'''

_ВЕРНО_ОБРЕЗАТЬ = '''
def обрезать(текст: str, предел_байт: int) -> str:
    if предел_байт < 0:
        raise ValueError("предел не может быть отрицательным")

    накоплено = 0
    куски: list[str] = []
    for символ in текст:
        вес = len(символ.encode("utf-8"))
        if накоплено + вес > предел_байт:
            break
        накоплено += вес
        куски.append(символ)
    return "".join(куски)
'''

_ИЗЪЯН_ОБРЕЗАТЬ = '''
def обрезать(текст: str, предел_байт: int) -> str:
    if предел_байт < 0:
        raise ValueError("предел не может быть отрицательным")
    return текст[:предел_байт]
'''


