def по_идентификаторам(идентификаторы: tuple[str, ...]) -> tuple[Задача, ...]:
    """Выбирает задачи по id. Неизвестный id — ошибка, а не молчаливый пропуск."""
    известные = {задача.id: задача for задача in ЗАДАЧИ}
    неизвестные = [и for и in идентификаторы if и not in известные]
    if неизвестные:
        raise KeyError(
            f"нет таких задач: {', '.join(неизвестные)}; "
            f"есть: {', '.join(известные)}"
        )
    return tuple(известные[и] for и in идентификаторы)
