"""Задачи-кандидаты: маленькие, но с ловушкой.

Требования к задаче:

1. Модель вероятно ошибётся — не «может быть», а по типовой привычке
   (встроенный round(), срез строки по символам вместо байтов, слияние
   касающихся интервалов).
2. Есть ДЕТЕРМИНИРОВАННЫЙ способ узнать правду: pytest-файл, который ловит
   именно этот изъян. Правду устанавливает прогон тестов, а не ещё одна модель.
3. Спецификация полная: судья, читая её, обязан иметь всё нужное, чтобы
   заметить изъян. Иначе мы измеряли бы не самопредпочтение, а неполноту спеки.
4. Кода мало — токены стоят денег.

Тест судье НЕ показывается: с тестом на руках задача становится тривиальной.

Заглушки (`заглушка_верная` / `заглушка_с_изъяном`) нужны только для --dry-run.
Это настоящий рабочий Python: в сухом прогоне якорь запускает по ним настоящий
pytest, поэтому проверяется весь конвейер, а не его имитация.
"""

