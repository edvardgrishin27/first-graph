# 2. Раздать сумму без потери копеек
# --------------------------------------------------------------------------

_СПЕКА_РАЗДАТЬ = """Модуль сохраняется как «решение.py». В нём одна функция:

    def раздать(сумма_копеек: int, частей: int) -> list[int]

Требования:
1. Длина результата — ровно `частей`.
2. Сумма элементов ТОЧНО равна сумма_копеек: ни одна копейка не теряется
   и не появляется из ниоткуда.
3. Части максимально ровные: разница между наибольшей и наименьшей — не больше 1.
4. Остаток уходит в начало списка: первые части больше или равны последующим.
5. Если частей меньше 1 или сумма_копеек отрицательная — ValueError.
6. Только стандартная библиотека."""

_ТЕСТ_РАЗДАТЬ = '''
import pytest

from решение import раздать


def test_остаток_уходит_в_начало():
    assert раздать(100, 3) == [34, 33, 33]


def test_сумма_сходится_всегда():
    for сумма in (0, 1, 7, 99, 100, 1001):
        for частей in (1, 2, 3, 7):
            части = раздать(сумма, частей)
            assert len(части) == частей
            assert sum(части) == сумма
            assert max(части) - min(части) <= 1


def test_плохие_аргументы():
    with pytest.raises(ValueError):
        раздать(100, 0)
    with pytest.raises(ValueError):
        раздать(-1, 3)
'''

_ВЕРНО_РАЗДАТЬ = '''
def раздать(сумма_копеек: int, частей: int) -> list[int]:
    if частей < 1:
        raise ValueError("частей должно быть не меньше одной")
    if сумма_копеек < 0:
        raise ValueError("сумма не может быть отрицательной")
    базовая, остаток = divmod(сумма_копеек, частей)
    return [базовая + (1 if индекс < остаток else 0) for индекс in range(частей)]
'''

_ИЗЪЯН_РАЗДАТЬ = '''
def раздать(сумма_копеек: int, частей: int) -> list[int]:
    if частей < 1:
        raise ValueError("частей должно быть не меньше одной")
    if сумма_копеек < 0:
        raise ValueError("сумма не может быть отрицательной")
    доля = round(сумма_копеек / частей)
    return [доля] * частей
'''


