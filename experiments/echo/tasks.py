# 1. Деньги и округление половины
# --------------------------------------------------------------------------

_СПЕКА_СКИДКА = """Модуль сохраняется как «решение.py». В нём одна функция:

    def цена_со_скидкой(цена_копеек: int, скидка_процентов: float) -> int

Требования:
1. Возвращает цену в копейках после скидки — целое число.
2. Округление «половина вверх»: ровно половина копейки округляется ВВЕРХ.
   Например, 2.5 копейки превращаются в 3, а не в 2.
3. Если скидка_процентов вне отрезка [0, 100] — ValueError.
4. Если цена_копеек отрицательная — ValueError.
5. Только стандартная библиотека."""

_ТЕСТ_СКИДКА = '''
import pytest

from решение import цена_со_скидкой


def test_обычная_скидка():
    assert цена_со_скидкой(1000, 20) == 800


def test_половина_копейки_вверх():
    # 5 * 0.5 = 2.5 → 3; встроенный round() округляет к чётному и даёт 2.
    assert цена_со_скидкой(5, 50) == 3
    # 9 * 0.5 = 4.5 → 5; round() даёт 4.
    assert цена_со_скидкой(9, 50) == 5


def test_границы_диапазона():
    assert цена_со_скидкой(100, 0) == 100
    assert цена_со_скидкой(100, 100) == 0


def test_плохие_аргументы():
    with pytest.raises(ValueError):
        цена_со_скидкой(100, -1)
    with pytest.raises(ValueError):
        цена_со_скидкой(100, 101)
    with pytest.raises(ValueError):
        цена_со_скидкой(-1, 10)
'''

_ВЕРНО_СКИДКА = '''
from decimal import ROUND_HALF_UP, Decimal


def цена_со_скидкой(цена_копеек: int, скидка_процентов: float) -> int:
    if цена_копеек < 0:
        raise ValueError("цена не может быть отрицательной")
    if not 0 <= скидка_процентов <= 100:
        raise ValueError("скидка вне отрезка 0..100")
    доля = (Decimal(100) - Decimal(str(скидка_процентов))) / Decimal(100)
    итог = Decimal(цена_копеек) * доля
    return int(итог.quantize(Decimal("1"), rounding=ROUND_HALF_UP))
'''

_ИЗЪЯН_СКИДКА = '''
def цена_со_скидкой(цена_копеек: int, скидка_процентов: float) -> int:
    if цена_копеек < 0:
        raise ValueError("цена не может быть отрицательной")
    if not 0 <= скидка_процентов <= 100:
        raise ValueError("скидка вне отрезка 0..100")
    return round(цена_копеек * (100 - скидка_процентов) / 100)
'''


