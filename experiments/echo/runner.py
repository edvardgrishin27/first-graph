"""Конвейер эксперимента: произвести код, узнать правду, собрать вердикты.

Порядок жёстко такой:

1. Исполнитель пишет код по спецификации. Изъян, если он будет, — его
   собственный. Чужой подложенный баг ничего не сказал бы о самопредпочтении.
2. Якорь запускает тесты и устанавливает правду: изъян есть или нет.
3. Один и тот же артефакт судят все четыре ячейки. Это парный план: B и D
   смотрят буквально на один и тот же код, поэтому разницу между ними нельзя
   списать на то, что одной ячейке попались задачи полегче.

Чистые артефакты тоже судятся — это контроль на снисходительность. Судья,
который принимает вообще всё, даст высокую долю одобрений на изъянном коде
безо всякого самопредпочтения, и без контроля мы бы этого не увидели.
"""

from __future__ import annotations

import time
from dataclasses import dataclass, field
from typing import Callable, Sequence

from experiments.echo import prompts
from experiments.echo.providers import ModelClient, ProviderError, Reply
from experiments.echo.tasks import Задача
from experiments.echo.truth import предполётная_проверка, проверить

# Строка плана: чья модель судит. Столбец: что судья видит.
СВОЯ = "своя"
ЧУЖАЯ = "чужая"
ОБЩИЙ = "общий"
ЧИСТЫЙ = "чистый"

ЯЧЕЙКИ: dict[str, tuple[str, str]] = {
    "A": (СВОЯ, ОБЩИЙ),
    "B": (СВОЯ, ЧИСТЫЙ),
    "C": (ЧУЖАЯ, ОБЩИЙ),
    "D": (ЧУЖАЯ, ЧИСТЫЙ),
}


@dataclass(frozen=True)
class Настройки:
    """Всё, что задаёт прогон. Пишется в файл результатов целиком."""

    задачи: tuple[str, ...]
    повторов: int
    исполнитель: str
    другая: str
    температура_исполнителя: float = 1.0
    температура_судьи: float = 0.0
    предел_токенов: int = 1200
    только_изъяны: bool = False
    пауза: float = 0.0
    seed: int = 1
    сухой_прогон: bool = False


@dataclass(frozen=True)
class Артефакт:
    """Один кусок кода, произведённый моделью, и правда о нём."""

    задача: str
    номер: int
    код: str
    ответ_исполнителя: str
    изъян: bool
    деталь: str


@dataclass(frozen=True)
class Суждение:
    """Вердикт одной ячейки по одному артефакту."""

    задача: str
    номер: int
    ячейка: str
    одобрено: bool | None
    ответ: str
    ошибка: str | None = None


@dataclass(frozen=True)
class Расход:
    """Токены по узлу графа: исполнитель и четыре судьи считаются раздельно.

    Разрез по узлам взят не для красоты: он ложится прямо на
    `first_graph.extras.invoice`, который раскладывает реальный счёт по узлам.
    """

    узел: str
    модель: str
    входных: int = 0
    выходных: int = 0
    вызовов: int = 0


@dataclass
class Результаты:
    """Сырые данные прогона. Сводит их report.py, считает stats.py."""

    настройки: Настройки
    исполнитель: str
    другая: str
    артефакты: list[Артефакт] = field(default_factory=list)
    суждения: list[Суждение] = field(default_factory=list)
    сбои: list[str] = field(default_factory=list)
    расходы: list[Расход] = field(default_factory=list)
    секунд: float = 0.0

    def изъянные(self) -> list[Артефакт]:
        return [а for а in self.артефакты if а.изъян]

    def чистые(self) -> list[Артефакт]:
        return [а for а in self.артефакты if not а.изъян]


def _учесть(
    результаты: Результаты, узел: str, модель: str, ответ: Reply
) -> None:
    """Прибавляет расход к нужному узлу, заменяя запись, а не правя на месте."""
    прежние = результаты.расходы
    подходит = [
        индекс
        for индекс, запись in enumerate(прежние)
        if запись.узел == узел and запись.модель == модель
    ]

    if not подходит:
        результаты.расходы = [
            *прежние,
            Расход(узел, модель, ответ.входных_токенов, ответ.выходных_токенов, 1),
        ]
        return

    место = подходит[0]
    было = прежние[место]
    стало = Расход(
        узел,
        модель,
        было.входных + ответ.входных_токенов,
        было.выходных + ответ.выходных_токенов,
        было.вызовов + 1,
    )
    результаты.расходы = [*прежние[:место], стало, *прежние[место + 1 :]]


def _произвести(
    задача: Задача,
    номер: int,
    исполнитель: ModelClient,
    настройки: Настройки,
    результаты: Результаты,
) -> Артефакт | None:
    """Шаг 1 и 2: модель пишет код, якорь устанавливает правду о нём."""
    try:
        ответ = исполнитель.complete(
            prompts.задание(задача),
            system=prompts.СИСТЕМА_ИСПОЛНИТЕЛЬ,
            temperature=настройки.температура_исполнителя,
            max_tokens=настройки.предел_токенов,
        )
    except ProviderError as exc:
        результаты.сбои.append(f"исполнитель {задача.id}#{номер}: {exc}")
        return None

    _учесть(результаты, "исполнитель", исполнитель.name, ответ)

    код = prompts.извлечь_код(ответ.text)
    if код is None:
        результаты.сбои.append(
            f"исполнитель {задача.id}#{номер}: в ответе не нашлось кода"
        )
        return None

    проверка = проверить(код, задача)
    return Артефакт(
        задача=задача.id,
        номер=номер,
        код=код,
        ответ_исполнителя=ответ.text,
        изъян=проверка.изъян,
        деталь=проверка.деталь,
    )


def _судить(
    артефакт: Артефакт,
    задача: Задача,
    ячейка: str,
    исполнитель: ModelClient,
    другая: ModelClient,
    настройки: Настройки,
    результаты: Результаты,
) -> Суждение:
    """Шаг 3: одна ячейка плана 2×2 выносит вердикт по артефакту."""
    строка, столбец = ЯЧЕЙКИ[ячейка]
    судья = исполнитель if строка == СВОЯ else другая
    сообщения = (
        prompts.ревью_общее(задача, артефакт.ответ_исполнителя)
        if столбец == ОБЩИЙ
        else prompts.ревью_чистое(задача, артефакт.код)
    )

    try:
        ответ = судья.complete(
            сообщения,
            system=prompts.СИСТЕМА_СУДЬЯ,
            temperature=настройки.температура_судьи,
            max_tokens=настройки.предел_токенов,
        )
    except ProviderError as exc:
        сообщение = f"судья {ячейка} {артефакт.задача}#{артефакт.номер}: {exc}"
        результаты.сбои.append(сообщение)
        return Суждение(
            артефакт.задача, артефакт.номер, ячейка, None, "", str(exc)
        )

    _учесть(результаты, f"судья {ячейка}", судья.name, ответ)
    вердикт = prompts.разобрать_вердикт(ответ.text)
    if вердикт is None:
        результаты.сбои.append(
            f"судья {ячейка} {артефакт.задача}#{артефакт.номер}: нет вердикта в ответе"
        )

    return Суждение(артефакт.задача, артефакт.номер, ячейка, вердикт, ответ.text)


def _метка(вердикт: bool | None) -> str:
    if вердикт is None:
        return "?"
    return "ПРИНЯТЬ" if вердикт else "ОТКЛОНИТЬ"


def прогнать(
    настройки: Настройки,
    задачи: Sequence[Задача],
    исполнитель: ModelClient,
    другая: ModelClient,
    вывод: Callable[[str], None] = print,
) -> Результаты:
    """Полный прогон плана 2×2. Возвращает сырые данные, ничего не интерпретируя."""
    предполётная_проверка()

    результаты = Результаты(
        настройки=настройки, исполнитель=исполнитель.name, другая=другая.name
    )
    начало = time.monotonic()

    вывод("ШАГ 1-2: модель пишет код, якорь запускает тесты")
    for задача in задачи:
        for номер in range(1, настройки.повторов + 1):
            артефакт = _произвести(задача, номер, исполнитель, настройки, результаты)
            if артефакт is None:
                вывод(f"  {задача.id}#{номер}: сорвалось, см. сбои")
                continue
            результаты.артефакты.append(артефакт)
            состояние = "ИЗЪЯН" if артефакт.изъян else "чисто"
            вывод(f"  {задача.id}#{номер}: {состояние} — {артефакт.деталь}")
            _пауза(настройки)

    по_id = {задача.id: задача for задача in задачи}
    на_суд = [
        артефакт
        for артефакт in результаты.артефакты
        if артефакт.изъян or not настройки.только_изъяны
    ]

    вывод("")
    вывод(f"ШАГ 3: четыре ячейки судят {len(на_суд)} артефактов")
    for артефакт in на_суд:
        решения = []
        for ячейка in ЯЧЕЙКИ:
            суждение = _судить(
                артефакт,
                по_id[артефакт.задача],
                ячейка,
                исполнитель,
                другая,
                настройки,
                результаты,
            )
            результаты.суждения.append(суждение)
            решения.append(f"{ячейка}:{_метка(суждение.одобрено)}")
            _пауза(настройки)
        пометка = "ИЗЪЯН" if артефакт.изъян else "чисто"
        вывод(
            f"  {артефакт.задача}#{артефакт.номер} ({пометка})  "
            + "  ".join(решения)
        )

    результаты.секунд = time.monotonic() - начало
    return результаты


def _пауза(настройки: Настройки) -> None:
    if настройки.пауза > 0:
        time.sleep(настройки.пауза)
