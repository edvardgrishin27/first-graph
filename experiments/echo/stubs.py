"""Заглушка вместо API: прогоняет весь конвейер, не тратя ни копейки.

ГЛАВНОЕ ПРО ЭТОТ ФАЙЛ. Судья-заглушка бросает честную монету — с одной и той
же вероятностью во всех четырёх ячейках. Она НАМЕРЕННО не воспроизводит эффект,
который мы ищем.

Соблазн был обратный: научить заглушку «подыгрывать», чтобы --dry-run печатал
красивую таблицу с подтверждённым тезисом. Тогда сухой прогон стал бы
демонстрацией желаемого, а не проверкой каркаса, и однажды его приняли бы за
результат. Поэтому монета честная, а вывод сухого прогона обязан звучать как
«эффект не воспроизвёлся» — так и должно быть на случайных данных.

Что в сухом прогоне НАСТОЯЩЕЕ: извлечение кода из ответа, запуск pytest якорем,
разбор вердикта, сведение таблицы, статистика. Заглушены только вызовы моделей.
"""

from __future__ import annotations

import random
from typing import Sequence

from experiments.echo.providers import Message, Reply
from experiments.echo.tasks import Задача

СИМВОЛОВ_НА_ТОКЕН = 4  # грубая оценка расхода, чтобы сухой прогон давал порядок
ДОЛЯ_ИЗЪЯНОВ = 0.6  # какую часть ответов исполнителя-заглушки делать изъянной
ДОЛЯ_ОДОБРЕНИЙ = 0.5  # честная монета судьи; одинакова во всех ячейках


class StubClient:
    """Детерминированная заглушка: тот же seed — тот же прогон."""

    def __init__(
        self,
        name: str,
        задачи: Sequence[Задача],
        seed: int = 1,
        доля_изъянов: float = ДОЛЯ_ИЗЪЯНОВ,
        доля_одобрений: float = ДОЛЯ_ОДОБРЕНИЙ,
    ) -> None:
        self.name = name
        self._задачи = tuple(задачи)
        self._seed = seed
        self._доля_изъянов = доля_изъянов
        self._доля_одобрений = доля_одобрений
        self._вызовов = 0

    def complete(
        self,
        messages: Sequence[Message],
        *,
        system: str,
        temperature: float,
        max_tokens: int,
    ) -> Reply:
        self._вызовов += 1
        случай = random.Random(f"{self._seed}|{self.name}|{self._вызовов}")
        текст_запроса = system + "\n".join(m.content for m in messages)

        if "ревьюер" in system:
            ответ = self._судья(случай)
        else:
            ответ = self._исполнитель(случай, текст_запроса)

        return Reply(
            ответ,
            len(текст_запроса) // СИМВОЛОВ_НА_ТОКЕН,
            len(ответ) // СИМВОЛОВ_НА_ТОКЕН,
        )

    def _исполнитель(self, случай: random.Random, запрос: str) -> str:
        задача = self._опознать(запрос)
        с_изъяном = случай.random() < self._доля_изъянов
        код = задача.заглушка_с_изъяном if с_изъяном else задача.заглушка_верная
        пометка = "Готово, стандартная библиотека."
        return f"{пометка}\n\n```python\n{код.strip()}\n```"

    def _судья(self, случай: random.Random) -> str:
        принять = случай.random() < self._доля_одобрений
        решение = "ПРИНЯТЬ" if принять else "ОТКЛОНИТЬ"
        return (
            "Прошёл по пунктам спецификации и граничным случаям.\n"
            f"ВЕРДИКТ: {решение}"
        )

    def _опознать(self, запрос: str) -> Задача:
        """Понимает, о какой задаче речь, по имени функции в спецификации."""
        for задача in self._задачи:
            if задача.функция in запрос:
                return задача
        raise ValueError(
            "заглушка не опознала задачу в запросе — имя функции из "
            "спецификации не нашлось ни в одной задаче"
        )
