"""Правда об изъяне: её устанавливает прогон тестов, а не ещё одна модель.

Весь эксперимент держится на этом файле. Если бы наличие изъяна определяла
третья модель, мы измеряли бы согласие моделей между собой — то есть ровно то,
в чём и подозреваем систематическую ошибку.

Поэтому здесь `anchor()` из ядра: код и тест кладутся во временную папку,
запускается настоящий pytest, решает код возврата. Якорь, который не смог
проверить, считается непройденным — это правило ядра, и оно тут работает
против нас, поэтому есть предполётная проверка (см. ниже).

БЕЗОПАСНОСТЬ. Мы запускаем код, который написала модель. Песочницы здесь нет:
временная папка и таймаут — не изоляция. Не запускайте эксперимент на машине,
где вам есть что терять.
"""

from __future__ import annotations

import sys
import tempfile
from dataclasses import dataclass
from pathlib import Path

from first_graph.anchors import anchor
from experiments.echo.tasks import МОДУЛЬ, Задача

ТАЙМАУТ_ТЕСТОВ = 60.0

КОМАНДА = [
    sys.executable,
    "-m",
    "pytest",
    "-q",
    "--no-header",
    "-p",
    "no:cacheprovider",
]

_ПРОБНЫЙ_МОДУЛЬ = "def два() -> int:\n    return 2\n"
_ПРОБНЫЙ_ТЕСТ = "from решение import два\n\n\ndef test_два():\n    assert два() == 2\n"


@dataclass(frozen=True)
class Проверка:
    """Итог прогона тестов над одним куском кода."""

    прошло: bool
    деталь: str

    @property
    def изъян(self) -> bool:
        return not self.прошло


def _прогнать(код: str, тест: str, таймаут: float) -> Проверка:
    with tempfile.TemporaryDirectory(prefix="echo-") as временная:
        папка = Path(временная)
        (папка / f"{МОДУЛЬ}.py").write_text(код, encoding="utf-8")
        (папка / f"test_{МОДУЛЬ}.py").write_text(тест, encoding="utf-8")

        состояние, прошло = anchor(
            {}, command=КОМАНДА, cwd=папка, timeout=таймаут
        )
        улика = состояние["anchors"][-1]
        return Проверка(прошло, улика.detail)


def проверить(код: str, задача: Задача, таймаут: float = ТАЙМАУТ_ТЕСТОВ) -> Проверка:
    """Есть ли в коде изъян, который ловит тест задачи."""
    return _прогнать(код, задача.тест, таймаут)


def предполётная_проверка(таймаут: float = ТАЙМАУТ_ТЕСТОВ) -> None:
    """pytest обязан запускаться — иначе «изъян» найдётся ровно везде.

    Правило ядра «непроверенное считается непройденным» здесь опасно: если
    pytest не установлен, якорь честно вернёт «не прошло» на любом коде, и
    прогон намеряет 100% изъянов. Проверяем один раз, на заведомо верном коде.
    """
    итог = _прогнать(_ПРОБНЫЙ_МОДУЛЬ, _ПРОБНЫЙ_ТЕСТ, таймаут)
    if not итог.прошло:
        raise RuntimeError(
            "предполётная проверка не прошла: заведомо верный код не проходит "
            f"тесты ({итог.деталь}). Скорее всего, не установлен pytest — "
            "тогда весь прогон намерял бы изъяны там, где их нет. "
            "Поставьте: pip install pytest"
        )
