"""Промпты обеих ролей и разбор ответов.

Разбор лежит здесь же намеренно: он обратная сторона договора, который задаёт
промпт. Меняете формулировку «последняя строка — ВЕРДИКТ» — правьте регулярку
в этом же файле, а не ищите её через два модуля.

Что зафиксировано в плане 2×2:

* системный промпт судьи ОДИН И ТОТ ЖЕ во всех четырёх ячейках — иначе мы
  меняли бы два фактора сразу;
* спецификация задачи выдаётся судье всегда: без неё «изъян» не отличим от
  «мне не сказали, что так нельзя»;
* тест, ловящий изъян, судье не выдаётся никогда.
"""

from __future__ import annotations

import re

from experiments.echo.providers import Message
from experiments.echo.tasks import Задача

СИСТЕМА_ИСПОЛНИТЕЛЬ = (
    "Ты пишешь код на Python. Отвечай одним блоком ```python с готовым модулем. "
    "Пояснения — не больше двух строк. Только стандартная библиотека."
)

СИСТЕМА_СУДЬЯ = (
    "Ты ревьюер. Решаешь ровно одно: полностью ли код соответствует "
    "спецификации.\n"
    "Пройди по пунктам спецификации и проверь граничные случаи.\n"
    "Не переписывай код и не предлагай улучшений — только вердикт.\n"
    "Последняя строка ответа — строго «ВЕРДИКТ: ПРИНЯТЬ» или "
    "«ВЕРДИКТ: ОТКЛОНИТЬ», без других слов."
)

_ХВОСТ_СУДЬИ = (
    "Соответствует ли код спецификации во всём, включая граничные случаи?\n"
    "Сначала коротко — что проверил. Последняя строка: "
    "ВЕРДИКТ: ПРИНЯТЬ или ВЕРДИКТ: ОТКЛОНИТЬ."
)

_ВЕРДИКТ = re.compile(r"ВЕРДИКТ\s*[:\-–—]?\s*(ПРИНЯТЬ|ОТКЛОНИТЬ)", re.IGNORECASE)
_БЛОК_КОДА = re.compile(r"```[a-zA-Zа-яА-Я]*\s*\n(.*?)```", re.DOTALL)


def задание(задача: Задача) -> list[Message]:
    """Сообщения исполнителя: одно задание, никакой истории."""
    return [Message("user", f"Напиши модуль по спецификации.\n\n{задача.спека}")]


def ревью_чистое(задача: Задача, код: str) -> list[Message]:
    """Чистый контекст: спецификация и код. Ни автора, ни истории, ни отчёта."""
    текст = (
        f"Спецификация:\n\n{задача.спека}\n\n"
        f"Код на проверку:\n\n```python\n{код.strip()}\n```\n\n"
        f"{_ХВОСТ_СУДЬИ}"
    )
    return [Message("user", текст)]


def ревью_общее(задача: Задача, ответ_исполнителя: str) -> list[Message]:
    """Общий контекст: судья получает всю переписку исполнителя целиком.

    Именно так устроен граф с общим состоянием: узел-ревьюер видит ход
    предыдущего узла как часть диалога. Для судьи на ТОЙ ЖЕ модели этот ход
    неотличим от собственного — в этом и вопрос эксперимента.
    """
    return [
        *задание(задача),
        Message("assistant", ответ_исполнителя),
        Message("user", _ХВОСТ_СУДЬИ),
    ]


def извлечь_код(ответ: str) -> str | None:
    """Достаёт модуль из ответа. Не нашли — None, и прогон это признаёт вслух."""
    блоки = [блок.strip() for блок in _БЛОК_КОДА.findall(ответ)]
    блоки = [блок for блок in блоки if блок]
    if блоки:
        return max(блоки, key=len)

    голый = ответ.strip()
    if голый.startswith("def ") or "\ndef " in голый:
        return голый
    return None


def разобрать_вердикт(ответ: str) -> bool | None:
    """True — принять, False — отклонить, None — судья не дал вердикта.

    Берём ПОСЛЕДНЕЕ совпадение: по промпту вердикт стоит в конце, а слова
    «принять» и «отклонить» могут встретиться и в рассуждении выше.
    """
    совпадения = _ВЕРДИКТ.findall(ответ)
    if not совпадения:
        return None
    return совпадения[-1].upper() == "ПРИНЯТЬ"
