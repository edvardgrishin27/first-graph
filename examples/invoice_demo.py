"""Люди узнают об этом из счёта. Лучше — до счёта.

Один и тот же прогон графа, посчитанный дважды: когда все узлы сидят на
флагманской модели, и когда рутина уехала на дешёвую.

    python3 examples/invoice_demo.py

Что здесь ЧЕСТНО, а что нет:
  честно — раскладка расхода по узлам и разница в тарифе при том же объёме;
  НЕ честно было бы назвать второй счёт предсказанием реального прогона:
  другая модель съест другое число токенов. Мы считаем только тариф.
"""

from __future__ import annotations

import sys
from decimal import Decimal
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from first_graph.extras.invoice import (  # noqa: E402
    Prices,
    Usage,
    invoice,
    what_if,
)

# Иллюстративный прайс. В настоящем расчёте — load_prices() из вашего файла
# с проставленной датой проверки.
ПРАЙС = Prices(
    checked_on="ИЛЛЮСТРАЦИЯ, не настоящие тарифы",
    currency="$",
    per_million={
        "флагман": (Decimal("15"), Decimal("75")),
        "средняя": (Decimal("3"), Decimal("15")),
        "быстрая": (Decimal("0.8"), Decimal("4")),
    },
)

# Отчёт из реального прогона: веер по семи источникам, сведение и судья.
ПРОГОН = [
    Usage("роутер",        "флагман", 1_500, 80),
    Usage("сбор_X",        "флагман", 42_000, 3_100),
    Usage("сбор_блоги",    "флагман", 51_000, 3_400),
    Usage("сбор_github",   "флагман", 38_000, 2_200),
    Usage("сбор_arxiv",    "флагман", 29_000, 1_900),
    Usage("сбор_reddit",   "флагман", 22_000, 1_500),
    Usage("дедуп",         "флагман", 18_000, 900),
    Usage("синтез",        "флагман", 34_000, 6_800),
    Usage("судья",         "флагман", 31_000, 1_200),
]

# Рутину — вниз, суждение — наверх. Синтез и судья остаются на флагмане.
ТИРИНГ = {
    "роутер": "быстрая",
    "сбор_X": "средняя",
    "сбор_блоги": "средняя",
    "сбор_github": "средняя",
    "сбор_arxiv": "средняя",
    "сбор_reddit": "средняя",
    "дедуп": "быстрая",
}


def main() -> int:
    всё_на_флагмане = invoice(ПРОГОН, ПРАЙС)
    с_тирингом = what_if(ПРОГОН, ПРАЙС, ТИРИНГ)

    print()
    print(всё_на_флагмане.render("СЧЁТ 1 · ВСЁ НА ФЛАГМАНЕ"))
    print()
    print(с_тирингом.render("СЧЁТ 2 · РУТИНА НА ДЕШЁВЫХ, СУЖДЕНИЕ НА ФЛАГМАНЕ"))

    разница = всё_на_флагмане.total - с_тирингом.total
    доля = разница / всё_на_флагмане.total * 100

    print()
    print("  ─────────────────────────────────────────────────")
    токенов = f"{с_тирингом.total_tokens:,}".replace(",", "\u00a0")
    print(f"  Тот же прогон. Те же {токенов} токенов.")
    print("  Разница только в том, какой узел на какой модели:")
    print(f"    было  ${всё_на_флагмане.total:.4f}")
    print(f"    стало ${с_тирингом.total:.4f}")
    print(f"    экономия ${разница:.4f} — это {доля:.0f}%")
    print()
    print("  Оговорка, без которой это была бы реклама: второй счёт — пересчёт")
    print("  по тарифу при том же объёме токенов, а не предсказание прогона.")
    print("  Другая модель съест другое их число. Порядок величины — виден.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
