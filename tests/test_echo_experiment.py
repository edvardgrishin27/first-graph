def test_судья_заглушка_бросает_честную_монету():
    клиент = StubClient("stub:проверка", ЗАДАЧИ, seed=1)
    вердикты = [
        prompts.разобрать_вердикт(
            клиент.complete(
                [Message("user", "код")],
                system=prompts.СИСТЕМА_СУДЬЯ,
                temperature=0.0,
                max_tokens=100,
            ).text
        )
        for _ in range(400)
    ]

    принято = sum(1 for в in вердикты if в)
    assert 0.4 < принято / len(вердикты) < 0.6


def test_заглушка_повторяема_при_одном_seed():
    сообщения = [Message("user", ЗАДАЧИ[0].спека)]

