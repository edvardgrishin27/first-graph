def test_сухой_прогон_доходит_до_конца():
    задачи = по_идентификаторам(("скидка",))
    настройки = Настройки(
        задачи=("скидка",),
        повторов=2,
        исполнитель="stub",
        другая="stub",
        сухой_прогон=True,
    )
    результаты = прогнать(
        настройки,
        задачи,
        StubClient("stub:своя", задачи, seed=3),
        StubClient("stub:чужая", задачи, seed=4),
        вывод=lambda _: None,
    )

    assert len(результаты.артефакты) == 2
    assert len(результаты.суждения) == 2 * len(ЯЧЕЙКИ)
    assert not результаты.сбои
    assert {с.ячейка for с in результаты.суждения} == set(ЯЧЕЙКИ)


def test_расход_считается_по_узлам_графа():
    задачи = по_идентификаторам(("скидка",))
    настройки = Настройки(
        задачи=("скидка",),
        повторов=2,
        исполнитель="stub",
        другая="stub",
        сухой_прогон=True,
    )
    результаты = прогнать(
        настройки,
        задачи,
        StubClient("stub:своя", задачи, seed=3),
        StubClient("stub:чужая", задачи, seed=4),
        вывод=lambda _: None,
    )

    узлы = {запись.узел: запись for запись in результаты.расходы}
    assert set(узлы) == {"исполнитель", "судья A", "судья B", "судья C", "судья D"}
    assert узлы["исполнитель"].вызовов == 2
    assert all(узлы[f"судья {я}"].вызовов == 2 for я in ЯЧЕЙКИ)
    # Строки плана обслуживают разные модели — это и есть фактор «своя/чужая».
    assert узлы["судья A"].модель == "stub:своя"
    assert узлы["судья D"].модель == "stub:чужая"


